class Solution {
  public:
    // Function to find the majority elements in the array
    vector<int> findMajority(vector<int>& arr) {
       map<int,int>mp;vector<int>v;int n=arr.size();
       for(auto it:arr){mp[it]++;}
       for(auto it:mp){
           if(it.second>n/3){v.push_back(it.first);}
       }
       return v;
    }
};

